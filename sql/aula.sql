
-- Estrutura da tabela aula
--

CREATE TABLE aula (
  id_aula int(11) AUTO_INCREMENT,
  id_componente int(11),
  id_turma int(11),
  id_horario int(11),
  id_docente int(11),
  id_juncao int(11),
  id_sala int(11),
  PRIMARY KEY (id_aula),
  CONSTRAINT fk_id_turma FOREIGN KEY (id_turma) REFERENCES turma (id_turma),
  CONSTRAINT fk_id_componente FOREIGN KEY (id_componente) REFERENCES componente (id_componente),
  CONSTRAINT fk_id_docente FOREIGN KEY (id_docente) REFERENCES utilizador (id_utilizador),
  CONSTRAINT fk_id_juncao FOREIGN KEY (id_juncao) REFERENCES juncao (id_juncao),
  CONSTRAINT fk_id_sala FOREIGN KEY (id_sala) REFERENCES sala (id_sala)
);

